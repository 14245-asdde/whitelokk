interface SocialIconProps {
  platform: string;
  url: string;
  color?: string;
}

export default function SocialIcon({ platform, url, color }: SocialIconProps) {
  const [hovered, setHovered] = useState(false);
  const [tooltipPos, setTooltipPos] = useState({ x: 0, y: 0 });
  const ref = useRef<HTMLAnchorElement>(null);
  const iconData = SOCIAL_ICONS[platform.toLowerCase()];
  if (!iconData) return null;

  const handleMouseEnter = () => {
    if (ref.current) {
      const rect = ref.current.getBoundingClientRect();
      setTooltipPos({
        x: rect.left + rect.width / 2,
        y: rect.top - 8,
      });
    }
    setHovered(true);
  };

  // Ensure URL has protocol
  const href = url.startsWith('http') ? url : `https://${url}`;

  const tooltip = hovered ? createPortal(
    <motion.div
      key={platform}
      initial={{ opacity: 0, y: 4, scale: 0.9 }}
      animate={{ opacity: 1, y: 0, scale: 1 }}
      exit={{ opacity: 0, y: 4, scale: 0.9 }}
      transition={{ duration: 0.1 }}
      style={{
        position: 'fixed',
        left: tooltipPos.x,
        top: tooltipPos.y,
        transform: 'translate(-50%, -100%)',
        zIndex: 99999,
        pointerEvents: 'none',
        whiteSpace: 'nowrap',
      }}
    >
      <div style={{
        background: 'rgba(10,10,10,0.97)',
        border: '1px solid rgba(255,255,255,0.15)',
        borderRadius: 8,
        padding: '5px 10px',
        fontSize: 12,
        color: '#fff',
        fontWeight: 600,
        boxShadow: '0 8px 24px rgba(0,0,0,0.5)',
      }}>
        {iconData.label}
      </div>
    </motion.div>,
    document.body
  ) : null;

  return (
    <>
      <a
        ref={ref}
        href={href}
        target="_blank"
        rel="noopener noreferrer"
        style={{
          position: 'relative',
          display: 'inline-flex',
          alignItems: 'center',
          justifyContent: 'center',
          width: 38,
          height: 38,
          borderRadius: 11,
          background: hovered ? 'rgba(255,255,255,0.12)' : 'rgba(255,255,255,0.06)',
          border: `1px solid ${hovered ? 'rgba(255,255,255,0.22)' : 'rgba(255,255,255,0.09)'}`,
          transition: 'all 0.15s ease',
          transform: hovered ? 'scale(1.12) translateY(-2px)' : 'scale(1)',
          textDecoration: 'none',
          cursor: 'pointer',
          zIndex: 1,
        }}
        onMouseEnter={handleMouseEnter}
        onMouseLeave={() => setHovered(false)}
        onClick={(e) => e.stopPropagation()}
      >
        <svg viewBox="0 0 24 24" fill="currentColor" style={{
          width: 16,
          height: 16,
          color: hovered ? (color || '#fff') : 'rgba(255,255,255,0.65)',
          transition: 'color 0.15s',
          pointerEvents: 'none',
        }}>
          <path d={iconData.svg} />
        </svg>
      </a>
      {tooltip}
    </>
  );
}
